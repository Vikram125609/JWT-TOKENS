const mongoose = require('mongoose');
const Schema = new mongoose.Schema({
    firstname: {
        type: String,
        required: true
    },
    lastname: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
    },
    password: {
        type: String,
        required: true
    },
    question: {
        type: String,
    },
    answer: {
        type: String,
        required: true
    },
    phone: {
        type: String,
        required: true
    },
    gender: {
        type: String,
        required: true
    }
},{timestamps:true});
const Models = mongoose.model("user",Schema);
module.exports = Models;
